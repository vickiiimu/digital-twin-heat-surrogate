"""PyTorch datasets and dataloaders for synthetic heat-diffusion data."""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path

import numpy as np
import torch
from torch.utils.data import DataLoader, Dataset


@dataclass(frozen=True)
class NormalizationStats:
    """Per-channel normalization statistics for X and scalar stats for Y."""

    x_mean: torch.Tensor
    x_std: torch.Tensor
    y_mean: torch.Tensor
    y_std: torch.Tensor

    @classmethod
    def from_arrays(cls, x: np.ndarray, y: np.ndarray) -> "NormalizationStats":
        x_tensor = torch.from_numpy(x).float()
        y_tensor = torch.from_numpy(y).float()
        return cls(
            x_mean=x_tensor.mean(dim=(0, 2, 3), keepdim=True),
            x_std=x_tensor.std(dim=(0, 2, 3), keepdim=True).clamp_min(1.0e-6),
            y_mean=y_tensor.mean(dim=(0, 2, 3), keepdim=True),
            y_std=y_tensor.std(dim=(0, 2, 3), keepdim=True).clamp_min(1.0e-6),
        )


class HeatDataset(Dataset):
    """Dataset for `.npz` files generated by `src/simulate.py`.

    Each item is a dictionary with ML-ready tensors:
      x: [3, H, W]
      y: [1, H, W]
      alpha: scalar thermal diffusivity

    If normalization stats are provided, x and y are standardized. Keeping this
    in the dataset makes the train/eval path simple and consistent.
    """

    def __init__(
        self,
        path: str | Path,
        stats: NormalizationStats | None = None,
        normalize: bool = True,
    ) -> None:
        self.path = Path(path)
        if not self.path.exists():
            raise FileNotFoundError(f"Missing dataset file: {self.path}")

        data = np.load(self.path)
        self.x = torch.from_numpy(data["X"]).float()
        self.y = torch.from_numpy(data["Y"]).float()
        self.alpha = torch.from_numpy(data["alpha"]).float()
        self.heater_mode = str(data["heater_mode"]) if "heater_mode" in data else "unknown"
        self.stats = stats
        self.normalize = normalize

        self._validate()

    def _validate(self) -> None:
        if self.x.ndim != 4 or self.x.shape[1] != 3:
            raise ValueError(f"Expected X shape [N, 3, H, W], got {tuple(self.x.shape)}")
        if self.y.ndim != 4 or self.y.shape[1] != 1:
            raise ValueError(f"Expected Y shape [N, 1, H, W], got {tuple(self.y.shape)}")
        if self.x.shape[0] != self.y.shape[0] or self.x.shape[0] != self.alpha.shape[0]:
            raise ValueError("X, Y, and alpha must have the same number of samples")
        if not torch.isfinite(self.x).all() or not torch.isfinite(self.y).all():
            raise ValueError(f"Found non-finite values in {self.path}")
        if self.normalize and self.stats is None:
            raise ValueError("normalize=True requires NormalizationStats")

    def __len__(self) -> int:
        return self.x.shape[0]

    def __getitem__(self, idx: int) -> dict[str, torch.Tensor]:
        x = self.x[idx]
        y = self.y[idx]

        if self.normalize:
            x = (x - self.stats.x_mean.squeeze(0)) / self.stats.x_std.squeeze(0)
            y = (y - self.stats.y_mean.squeeze(0)) / self.stats.y_std.squeeze(0)

        return {
            "x": x,
            "y": y,
            "alpha": self.alpha[idx],
        }

    def denormalize_y(self, y: torch.Tensor) -> torch.Tensor:
        """Convert normalized model outputs back to physical temperature units."""
        if not self.normalize or self.stats is None:
            return y
        return y * self.stats.y_std.to(y.device) + self.stats.y_mean.to(y.device)


def load_normalization_stats(data_dir: str | Path = "data") -> NormalizationStats:
    """Compute normalization stats from the training split only."""
    train_path = Path(data_dir) / "train.npz"
    if not train_path.exists():
        raise FileNotFoundError(f"Missing training split: {train_path}")
    data = np.load(train_path)
    return NormalizationStats.from_arrays(data["X"], data["Y"])


def make_dataloaders(
    data_dir: str | Path = "data",
    batch_size: int = 32,
    num_workers: int = 0,
    normalize: bool = True,
    include_ood: bool = True,
) -> tuple[dict[str, DataLoader], NormalizationStats | None]:
    """Create train/val/test/OOD dataloaders for model training and evaluation."""
    data_dir = Path(data_dir)
    stats = load_normalization_stats(data_dir) if normalize else None
    split_names = ["train", "val", "test"]
    if include_ood:
        split_names += ["ood_corner", "ood_multi"]

    loaders: dict[str, DataLoader] = {}
    for split in split_names:
        path = data_dir / f"{split}.npz"
        dataset = HeatDataset(path, stats=stats, normalize=normalize)
        loaders[split] = DataLoader(
            dataset,
            batch_size=batch_size,
            shuffle=(split == "train"),
            num_workers=num_workers,
            pin_memory=torch.cuda.is_available(),
        )
    return loaders, stats


